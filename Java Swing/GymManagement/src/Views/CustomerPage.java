    private void btnCancelMemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelMemActionPerformed
        try {
            // TODO add your handling code here:
            int i = Customer.updateMembership(SDT_cus);
            if (i > 0) {
                int btn = JOptionPane.showConfirmDialog(this, "Bạn có muốn hủy dịch vụ đang sử dụng?", "Hủy dịch vụ", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
                if (btn == JOptionPane.YES_OPTION) {
//                    Membership.deleteMembership();
                    JOptionPane.showMessageDialog(this, "Hủy dịch vụ thành công!");
                    txtMembership.setText("Khách hàng CHƯA ĐĂNG KÝ dịch vụ");
                    btnCancelMem.setEnabled(false);
                    btnExport.setEnabled(false);
                } else if (btn == JOptionPane.NO_OPTION) {
                    remove(btn);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Hủy dịch vụ thất bại", "Lỗi", JOptionPane.ERROR_MESSAGE);
            }
            
        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(CustomerPage.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnCancelMemActionPerformed

