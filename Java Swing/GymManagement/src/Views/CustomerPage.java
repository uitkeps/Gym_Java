    private void getCustomerPage() throws ClassNotFoundException, ParseException {
        try {
            
            ResultSet rs = Customer.getCustomerforPage();
            ResultSet rs1 = Customer.getMembershipforPage(SDT_cus);
            while (rs.next()) {
                txtName.setText(rs.getString("cus_name"));
                txtName1.setText(rs.getString("cus_name"));
                dcBirthday.setDate(rs.getDate("cus_birthday"));
                txtGender.setText(rs.getString("cus_gender"));
                txtType.setText(rs.getString("cus_type"));
                dcDateJoin.setDate(rs.getDate("cus_datejoin"));
                txtTelephone.setText(rs.getString("cus_telephone"));
                txtAddress.setText(rs.getString("cus_address"));
                txtWeight.setText(rs.getString("cus_weight"));
                txtHeight.setText(rs.getString("cus_height"));
                if (rs.getDate("cus_memexpired") != null) {
                    dcExpired.setVisible(true);
                    lbExpired.setText("Ngày hết hạn: ");
                    dcExpired.setDate(rs.getDate("cus_memexpired"));
                    btnCancelMem.setEnabled(true);
                    btnExport.setEnabled(true);
                } else {
                    dcExpired.setVisible(false);
                    lbExpired.setText("SĐT +84" + SDT_cus + " chưa đăng ký dịch vụ");
                    btnCancelMem.setEnabled(false);
                    btnExport.setEnabled(false);
                }
                txtRevenue.setText(rs.getString("cus_revenue"));
            }
            if (rs1.next()) {
                txtMembership.setText(rs1.getString("mem_bonus"));
            }
        } catch (HeadlessException | SQLException ex) {
            JOptionPane.showMessageDialog(this, ex, "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }

