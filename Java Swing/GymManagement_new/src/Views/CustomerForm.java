    private void getRowCustomer(int index) {
        try {
            index = tbCustomer.getSelectedRow();
            MaKH = (String) tbCustomer.getValueAt(index, 0);
            txtName.setText((String) tbCustomer.getValueAt(index, 1));
            Date ngS = new SimpleDateFormat("dd/MM/yyyy").parse((String) tbCustomer.getValueAt(index, 2));
            dcBirthday.setDate(ngS);

            String gioiTinh = (String) tbCustomer.getValueAt(index, 3);
            if (gioiTinh.equals("Nam")) {
                radioBtnMale.setSelected(true);
            } else {
                radioBtnFemale.setSelected(true);
            }
            Date ngVL = new SimpleDateFormat("dd/MM/yyyy").parse((String) tbCustomer.getValueAt(index, 4));
            dcDateJoin.setDate(ngVL);
            txtAddress.setText((String) tbCustomer.getValueAt(index, 5));
            txtTelephone.setText((String) tbCustomer.getValueAt(index, 6));
            txtHeight.setText((String) tbCustomer.getValueAt(index, 7));
            txtWeight.setText((String) tbCustomer.getValueAt(index, 8));

            String loaiKH = (String) tbCustomer.getValueAt(index, 9);
            switch (loaiKH) {
                case "Thân thiết" -> {
                    radBtnThanThiet.setSelected(true);
                    radBtnVIP.setEnabled(false);
                    radBtnSuperVIP.setEnabled(false);
                }
                case "VIP" -> {
                    radBtnVIP.setSelected(true);
                    radBtnThanThiet.setEnabled(false);
                    radBtnSuperVIP.setEnabled(false);
                }
                case "Super VIP" -> {
                    radBtnSuperVIP.setSelected(true);
                    radBtnThanThiet.setEnabled(false);
                    radBtnSuperVIP.setEnabled(false);
                }
                default ->
                    throw new AssertionError();
            }

//            Date ngHH = new SimpleDateFormat("dd/MM/yyyy").parse((String) tbCustomer.getValueAt(index, 10));
//            dcExpired.setDate(ngHH);
            txtExpired.setText((String) tbCustomer.getValueAt(index, 10));
            txtRevenue.setText((String) tbCustomer.getValueAt(index, 11));

        } catch (ParseException ex) {
            Logger.getLogger(CustomerForm.class.getName()).log(Level.SEVERE, null, ex);
            System.out.println(ex);
        }
    }

    private void setTableCustomer() {
        try {
            Customer kh = new Customer();
            ResultSet rs = kh.insertTableCustomer();
            tbModelCustomer = (DefaultTableModel) tbCustomer.getModel();
            tbModelCustomer.setRowCount(0);
            while (rs.next()) {
                String maKH = rs.getString("cus_id");
                String tenKH = rs.getString("cus_name");
                Date ngS = rs.getDate("cus_birthday");
                String sNgS = String.format("%1$td/%1$tm/%1$tY", ngS);
                String gioiTinh = rs.getString("cus_gender");
                Date ngTG = rs.getDate("cus_datejoin");
                String sNgTG = String.format("%1$td/%1$tm/%1$tY", ngTG);
                String diaChi = rs.getString("cus_address");
                String SDT = rs.getString("cus_telephone");
                String chieuCao = rs.getString("cus_height");
                String canNang = rs.getString("cus_weight");
                String loaiKH = rs.getString("cus_type");
                Date ngHH = rs.getDate("cus_memexpired");
                String sNgHH = "";
                if (!rs.wasNull()) {
                    sNgHH = String.format("%1$td/%1$tm/%1$tY", ngHH);
                }
                String tichLuy = rs.getString("cus_revenue");
                tbModelCustomer.addRow(new Object[]{
                    maKH, tenKH, sNgS, gioiTinh, sNgTG, diaChi, SDT, chieuCao, canNang, loaiKH, sNgHH, tichLuy
                });
            }
        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(StaffForm.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

