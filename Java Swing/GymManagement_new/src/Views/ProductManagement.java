    private boolean checkNull() {
        if (String.valueOf(txtName.getText()).length() == 0) {
            JOptionPane.showMessageDialog(this, "Thông tin không hợp lệ", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }
//        if (String.valueOf(txtDate.getText()).length() == 0) {
//            JOptionPane.showMessageDialog(this, "Thông tin không hợp lệ", "Lỗi", JOptionPane.ERROR_MESSAGE);
//            return false;
//        }
        if (String.valueOf(txtPrice.getText()).length() == 0) {
            JOptionPane.showMessageDialog(this, "Thông tin không hợp lệ", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (String.valueOf(cbxType.getSelectedItem().toString()).length() == 0) {
            JOptionPane.showMessageDialog(this, "Thông tin không hợp lệ", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (String.valueOf(txtAmount.getText()).length() == 0) {
            JOptionPane.showMessageDialog(this, "Thông tin không hợp lệ", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (String.valueOf(txtAmount.getText()).equals("0")) {
            JOptionPane.showMessageDialog(this, "Số lượng sản phẩm nhập vào phải lớn hơn 0", "Sản phẩm không hợp lệ", JOptionPane.ERROR_MESSAGE);
        }
        return true;
    }

