    public void setTableProduct() {
        try {
            Connection conn = ConnectionUtils.getOracleConnection();
            String sql = "SELECT * FROM product ORDER BY product_id";
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            modelProduct = (DefaultTableModel) tableProduct.getModel();
            while (rs.next()) {
                String ma = rs.getString("product_id");
                String tenHangHoa = rs.getString("product_name");
                String giaTien = rs.getString("product_price");
                String loaiHangHoa = rs.getString("product_type");
                String soLuong = rs.getString("product_amount");
                if ("0".equals(soLuong)) {
                    soLuong = "Hết hàng";
                }
                modelProduct.addRow(new Object[]{ma, tenHangHoa, giaTien, loaiHangHoa, soLuong});
            }
        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(ProductManagement.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

