    private void getSupportForCustomer() throws IOException {

        try {
            try ( // Đọc từ file rồi ghi vào 1 biến String
                     FileInputStream input = new FileInputStream("src\\Customer.txt")) {
                int i = 0;
                while ((i = input.read()) != -1) {
                    SDT_cus += (char) i;
                }
            }
            Connection conn = ConnectionUtils.getOracleConnection();

            String sql = "SELECT cus_name FROM customer where cus_telephone = '" + SDT_cus + "' ";
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            
            while (rs.next()) {
                txtName1.setText(rs.getString("cus_name"));
            }
        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(MuaHang.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

