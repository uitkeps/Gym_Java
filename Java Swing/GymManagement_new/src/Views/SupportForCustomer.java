    private void btnSendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendActionPerformed
        txtMessage.setEditable(true);
        btnSend.setEnabled(true);
        try {
            if ("".equals(txtMessage.getText())) {
                JOptionPane.showMessageDialog(this, "Tin nhắn không được để trống", "Tin nhắn", JOptionPane.WARNING_MESSAGE);
            } else {
                os = new DataOutputStream(socket.getOutputStream());
                os.writeUTF(txtMessage.getText());
                os.flush();
                model.addElement("[" + LocalDateTime.now() + "]: " + txtName1.getText() + ": " + txtMessage.getText());
                lsHistory.setModel(model);
                txtMessage.setText("");
            }
        } catch (IOException ex) {
            Logger.getLogger(SupportForCustomer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSendActionPerformed

    private void btnStartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnStartActionPerformed

        txtMessage.setEditable(true);
        btnSend.setEnabled(true);

        model.addElement("[" + LocalDateTime.now() + "]: " + txtName1.getText() + " đang kết nối...");
        lsHistory.setModel(model);

        try {
            socket = new Socket("localhost", Integer.parseInt(txtPort.getText()));
        } catch (IOException ex) {
            Logger.getLogger(SupportForCustomer.class.getName()).log(Level.SEVERE, null, ex);
            model.addElement("[" + LocalDateTime.now() + "]: " + "ADMIN đang bận, vui lòng thử lại sau!");
            lsHistory.setModel(model);
            return;
        }
        model.addElement("[" + LocalDateTime.now() + "]: " + txtName1.getText() + " kết nối THÀNH CÔNG!");
        lsHistory.setModel(model);
        Thread t = new Thread(SupportForCustomer.this);
        t.start();
        btnStart.setEnabled(false);

    }//GEN-LAST:event_btnStartActionPerformed

